#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(void *))
{
	if (!lst || !f)
		return ;
	while (lst)
	{
		if (lst->type == TYPE_PTR)
			f(lst->u_ctt.content);
		lst = lst->next;
	}
}
