#include "libft.h"
#include <stdlib.h>

void	ft_lstclear(t_list **lst, void (*del)(void *))
{
	t_list	*tmp;

	if (!lst || !*lst)
		return ;
	while (*lst)
	{
		tmp = (*lst)->next;
		if ((*lst)->type == TYPE_PTR && del)
			del((*lst)->u_ctt.content);
		free(*lst);
		*lst = tmp;
	}
}
