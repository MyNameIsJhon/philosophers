#include "libft.h"
#include <stdlib.h>

t_list	*ft_lstnew(void *content)
{
	t_list	*lst;

	lst = (t_list *)malloc(sizeof(t_list));
	if (!lst)
		return (NULL);
	lst->u_ctt.content = content;
	lst->next = NULL;
	lst->type = TYPE_PTR;
	return (lst);
}
