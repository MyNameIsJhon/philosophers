#include "libft.h"
#include <stdlib.h>

void	ft_lstdelone(t_list *lst, void (*del)(void*))
{
	if (!lst)
		return ;
	if (lst->type == TYPE_PTR && del)
		del(lst->u_ctt.content);
	free(lst);
}
